class MultiFor{
	public static void main(String[] args) {
		for(int x = 0; x < 4; x++){
			for(int y= 4;y>2;y--){
				System.out.println(x + "" + y);
			}
			if(x == 1){
				x++;
			}
		}
	}
}


x = x + 3; // 54 6

x = x + 6; // 60 10


x = x + 2; // 45 6


x++; // 36 6


x--; // 18 6

x = x + 0; // 6 14

